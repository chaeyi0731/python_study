#정수와 실수로 계산하는 것이 중요하다
#정수형(int) 실수형(float)
# 연산자 덧셈 (+) / 뺼셈(-) / 곱셉(*)/ 나눗셈 (/) / 나머지 (%) / 몫 (//) / 거듭제곱(**)

print(3 + 3)
print(3 - 3)
print(3 * 3)
print(3 / 3)

#결과 6 0 9 1.0

#나누기 연산자는 소숫점이 있든 없든 무조건 실수 타입으로 표시한다

print(5 / 3)

#무한대로 늘어나는 소숫점은 파이썬에서 간결하게 표기한다

