#거듭제곱 (승)은 **를 사용한다 결과 27 과 8

print(5/2)

#결과는 5/2로 나눠떨어지지 않는다

print(5%2)

#몫을 사용시 1로 떨어진다.
#5가 홀수라는 뜻이 되기도 한다

