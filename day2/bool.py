#불리언은 참과 거짓을 나타내는 데이터 타입
#조건문,반복문,논리 연산 등에 사용된다.

print(type(True))

#결과 <class 'bool'> bool은 boolean 의 줄임말

#비교 연산자를 사용하여 숫자형의 크기를 알 수 있다.
#비교 연산의 결과는 불리언 (true) or (False)이다.

print(100000 > 200000) #크다
print(100000 < 200000) #작다
print(100000 == 200000) #같다
print(100000 != 200000) #같지않다

# 결과 
#False
#True
#False
#True
